/**
 * GitHub Actions security workflow content for scaffolded projects.
 * @param {string} backend - backend stack key or custom text
 * @returns {string}
 */
function securityWorkflowYaml(backend) {
  const key = resolveBackendKey(backend);
  const auditSteps = auditStepsForBackend(key);

  return `# Generated by agentic-crew — dependency and vulnerability scanning
name: Security

on:
  push:
    branches: [main, master]
  pull_request:
    branches: [main, master]
  schedule:
    - cron: '0 9 * * 1'

permissions:
  contents: read

jobs:
  audit:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
${auditSteps}
`;
}

/**
 * @param {string} backend
 * @returns {'nodejs' | 'python' | 'go' | 'rust' | 'generic'}
 */
function resolveBackendKey(backend) {
  const b = String(backend || '').toLowerCase();
  if (b === 'nodejs' || b.includes('node')) return 'nodejs';
  if (b === 'python' || b.includes('python') || b.includes('django') || b.includes('fastapi')) {
    return 'python';
  }
  if (b === 'go' || b === 'golang') return 'go';
  if (b === 'rust') return 'rust';
  return 'generic';
}

/**
 * @param {'nodejs' | 'python' | 'go' | 'rust' | 'generic'} key
 * @returns {string}
 */
function auditStepsForBackend(key) {
  switch (key) {
    case 'nodejs':
      return `      - uses: actions/setup-node@v4
        with:
          node-version: '20'
          cache: npm
      - run: npm ci
      - run: npm audit --audit-level=high
`;
    case 'python':
      return `      - uses: actions/setup-python@v5
        with:
          python-version: '3.12'
      - run: pip install pip-audit
      - run: |
          if [ -f requirements.txt ]; then pip-audit -r requirements.txt; else pip-audit; fi
`;
    case 'go':
      return `      - uses: actions/setup-go@v5
        with:
          go-version: 'stable'
      - run: go install golang.org/x/vuln/cmd/govulncheck@latest
      - run: govulncheck ./...
`;
    case 'rust':
      return `      - uses: actions/setup-rust@v1
      - run: cargo install cargo-audit --locked 2>/dev/null || true
      - run: cargo audit
`;
    default:
      return `      - name: Configure stack-specific scanning
        run: |
          echo "Add npm audit, pip-audit, govulncheck, or cargo audit for your stack."
          echo "https://owasp.org/www-community/Component_Analysis"
`;
  }
}

module.exports = { securityWorkflowYaml, resolveBackendKey };
